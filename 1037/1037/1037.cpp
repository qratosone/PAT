#include "stdafx.h"
#include   <iostream>   
#include   <cstdlib>   
//是赋值以逗号为分隔符  
using   namespace   std;

int   main()
{
	int   a, b, c,d,e,f;
	cin >> a; cin.ignore(1, ', ');
	cin >> b; cin.ignore(1, ', ');
	cin >> c; cin.ignore(1, ', ');
	cin >> d; cin.ignore(1, ', ');
	cin >> e; cin.ignore(1, ', ');
	cin >> f; cin.ignore(1, ', ');
	cout << a << endl;
	cout << b << endl;
	cout << c << endl;
	cout << d << endl;
	cout << e << endl;
	cout << f << endl;
	system("pause ");
	return   0;
}
